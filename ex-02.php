<?php

// Conversor de Temperatura

$celsius = 100;
$kelvin = $celsius + 273;
$fahrenheit = ($celsius * 9 / 5) + 32;

echo 'Temperatura em Celsius: ' . $celsius . 'ºC';
echo '<hr/>';
echo 'Temperatura em Kelvin: ' . $kelvin . ' K';
echo '<hr/>';
echo 'Temperatura em Fahrenheit: ' . $fahrenheit . 'ºF';
